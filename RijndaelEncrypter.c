/* MixColumns multiplication matrixes. */

static MATRIX MixColumnMatrix =
{
	{ 0x02, 0x03, 0x01, 0x01 },
	{ 0x01, 0x02, 0x03, 0x01 },
	{ 0x01, 0x01, 0x02, 0x03 },
	{ 0x03, 0x01, 0x01, 0x02 }
};

static MATRIX InvMixColumnMatrix =
{
	{ 0x0E, 0x0B, 0x0D, 0x09 },
	{ 0x09, 0x0E, 0x0B, 0x0D },
	{ 0x0D, 0x09, 0x0E, 0x0B },
	{ 0x0B, 0x0D, 0x09, 0x0E }
};

/* Static helper methods. */

static void GetDataRow(DATA_ROW abRow, DATA_MATRIX abMatrix, int iRow)
{
	CopyBytes(abRow, abMatrix[iRow], DATA_COLUMNS);
}

static void GetDataColumn(DATA_COLUMN abColumn, DATA_MATRIX abMatrix, int jColumn)
{
	int i;

	for (i = 0; i < ROWS; i++)
		abColumn[i] = abMatrix[i][jColumn];
}

static void SetDataRow(DATA_MATRIX abMatrix, DATA_ROW abRow, int iRow)
{
	CopyBytes(abMatrix[iRow], abRow, DATA_COLUMNS);
}

static void SetDataColumn(DATA_MATRIX abMatrix, DATA_COLUMN abColumn, int jColumn)
{
	int i;

	for (i = 0; i < ROWS; i++)
		abMatrix[i][jColumn] = abColumn[i];
}

static void ConvertDataVectorToMatrix(DATA_MATRIX abMatrix, DATA_VECTOR abVector)
{
	int jDest, iSource;

	for (jDest = 0, iSource = 0; jDest < DATA_COLUMNS; jDest++, iSource += ROWS)
		SetDataColumn(abMatrix, &abVector[iSource], jDest);
}

static void ConvertDataMatrixToVector(DATA_VECTOR abVector, DATA_MATRIX abMatrix)
{
	int jSource, iDest;

	for (jSource = 0, iDest = 0; jSource < DATA_COLUMNS; jSource++, iDest += ROWS)
		GetDataColumn(&abVector[iDest], abMatrix, jSource);
}

static void SubstituteMatrix(DATA_MATRIX abState)
{
	int i;

	for (i = 0; i < ROWS; i++)
		SubstituteBytes(abState[i], abState[i], S, DATA_COLUMNS);
}

static void InvSubstituteMatrix(DATA_MATRIX abState)
{
	int i;

	for (i = 0; i < ROWS; i++)
		SubstituteBytes(abState[i], abState[i], InvS, DATA_COLUMNS);
}

static void ShiftRowsMatrix(DATA_MATRIX abState)
{
	RotateLeftBytes(abState[1], abState[1], 1, DATA_COLUMNS);
	if (DATA_COLUMNS <= 6)
	{
		RotateLeftBytes(abState[2], abState[2], 2, DATA_COLUMNS);
		RotateLeftBytes(abState[3], abState[3], 3, DATA_COLUMNS);
	}
	else
	{
		RotateLeftBytes(abState[2], abState[2], 3, DATA_COLUMNS);
		RotateLeftBytes(abState[3], abState[3], 4, DATA_COLUMNS);
	}
}

static void InvShiftRowsMatrix(DATA_MATRIX abState)
{
	RotateRightBytes(abState[1], abState[1], 1, DATA_COLUMNS);
	if (DATA_COLUMNS <= 6)
	{
		RotateRightBytes(abState[2], abState[2], 2, DATA_COLUMNS);
		RotateRightBytes(abState[3], abState[3], 3, DATA_COLUMNS);
	}
	else
	{
		RotateRightBytes(abState[2], abState[2], 3, DATA_COLUMNS);
		RotateRightBytes(abState[3], abState[3], 4, DATA_COLUMNS);
	}
}

static void MixColumnsMatrix(DATA_MATRIX abState)
{
	int j;
	VECTOR abTemp, abResult;

	for (j = 0; j < DATA_COLUMNS; j++)
	{
		GetDataColumn(abTemp, abState, j);
		MulVector(abResult, abTemp, MixColumnMatrix);
		SetDataColumn(abState, abResult, j);
	}
}

static void InvMixColumnsMatrix(DATA_MATRIX abState)
{
	int j;
	VECTOR abTemp, abResult;

	for (j = 0; j < DATA_COLUMNS; j++)
	{
		GetDataColumn(abTemp, abState, j);
		MulVector(abResult, abTemp, InvMixColumnMatrix);
		SetDataColumn(abState, abResult, j);
	}
}

