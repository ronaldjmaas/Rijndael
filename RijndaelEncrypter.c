/* Implementation. */

static void ExpandKey(RIJNDAEL_ENCRYPTER* pEncrypter, KEY_VECTOR abKey)
{
	DATA_COLUMN abRoundConstant =
	{
		0x01, 0x00, 0x00, 0x00
	};
	BYTE abTempMatrix[ROWS][EXPANDED_KEY_COLUMNS];
	int iDest, jDest;
	int iSource;
	int iRound;
	DATA_COLUMN abTempColumn;
	int i;

	for (jDest = 0, iSource = 0; jDest < KEY_COLUMNS; jDest++)
	{
		for (iDest = 0; iDest < ROWS; iDest++)
			abTempMatrix[iDest][jDest] = abKey[iSource++];
	}

	for (jDest = KEY_COLUMNS; jDest < EXPANDED_KEY_COLUMNS; jDest++)
	{
		for (i = 0; i < ROWS; i++)
			abTempColumn[i] = abTempMatrix[i][jDest - 1];

		if (jDest % KEY_COLUMNS == 0)
		{
			RotateLeftBytes(abTempColumn, abTempColumn, 1, ROWS);
			SubstituteBytes(abTempColumn, abTempColumn, S, ROWS);
			XorBytes(abTempColumn, abTempColumn, abRoundConstant, ROWS);

			abRoundConstant[0] = MulByte(abRoundConstant[0], 0x02);
		}
		else if (KEY_COLUMNS > 6 && jDest % KEY_COLUMNS == 4)
		{
			SubstituteBytes(abTempColumn, abTempColumn, S, ROWS);
		}

		for (i = 0; i < ROWS; i++)
			abTempMatrix[i][jDest] = abTempMatrix[i][jDest - KEY_COLUMNS] ^ abTempColumn[i];
	}

	for (iRound = 0; iRound < ROUNDS + 1; iRound++)
	{
		for (iDest = 0; iDest < ROWS; iDest++)
		{
			jDest = iRound * DATA_COLUMNS;
			SetDataRow(pEncrypter->m_abExpandedKeyMatrix[iRound], &abTempMatrix[iDest][jDest], iDest);
		}
	}
}

static void AddRoundKey(RIJNDAEL_ENCRYPTER* pEncrypter, DATA_MATRIX abState, int iRound)
{
	int i;

	for (i = 0; i < ROWS; i++)
		XorBytes(abState[i], pEncrypter->m_abExpandedKeyMatrix[iRound][i], abState[i], DATA_COLUMNS);
}

/* Initialize cipher with a new key. */

void Init(RIJNDAEL_ENCRYPTER* pEncrypter, KEY_VECTOR abKey)
{
	Clear(pEncrypter);
	ExpandKey(pEncrypter, abKey);
}

/* Clear any key material. */

void Clear(RIJNDAEL_ENCRYPTER* pEncrypter)
{
	int iRound, iRow;

	for (iRound = 0; iRound < ROUNDS + 1; iRound++)
	{
		for (iRow = 0; iRow < ROWS; iRow++)
			ClearBytes(pEncrypter->m_abExpandedKeyMatrix[iRound][iRow], DATA_COLUMNS);
	}
}

/* Encrypt a data block. */

void Encrypt(RIJNDAEL_ENCRYPTER* pEncrypter, DATA_VECTOR abOutput, DATA_VECTOR abInput)
{
	DATA_MATRIX abState;
	int i;

	ConvertDataVectorToMatrix(abState, abInput);

	AddRoundKey(pEncrypter, abState, 0);

	for (i = 0; i < ROUNDS - 1; i++)
	{
		SubstituteMatrix(abState);
		ShiftRowsMatrix(abState);
		MixColumnsMatrix(abState);
		AddRoundKey(pEncrypter, abState, i + 1);
	}
	
	SubstituteMatrix(abState);
	ShiftRowsMatrix(abState);
	AddRoundKey(pEncrypter, abState, ROUNDS);

	ConvertDataMatrixToVector(abOutput, abState);
}

/* Decrypt a data block. */

void Decrypt(RIJNDAEL_ENCRYPTER* pEncrypter, DATA_VECTOR abOutput, DATA_VECTOR abInput)
{
	DATA_MATRIX abState;
	int i;

	ConvertDataVectorToMatrix(abState, abInput);

	AddRoundKey(pEncrypter, abState, ROUNDS);
	InvShiftRowsMatrix(abState);
	InvSubstituteMatrix(abState);

	for (i = ROUNDS - 1; i >= 1; i--)
	{
		AddRoundKey(pEncrypter, abState, i);
		InvMixColumnsMatrix(abState);
		InvShiftRowsMatrix(abState);
		InvSubstituteMatrix(abState);
	}
	
	AddRoundKey(pEncrypter, abState, 0);

	ConvertDataMatrixToVector(abOutput, abState);
}
